        private void tampildata(){
        try{
            java.sql.Statement stat = (java.sql.Statement) dbKoneksi.getKoneksi().createStatement();
            rs = stat.executeQuery("SELECT * FROM tbakun");

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("Username");
            model.addColumn("Nama");
            model.addColumn("Password");
            model.addColumn("Role");
            
            int no = 1 ;
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("username"),
                    rs.getString("nama"),
                    rs.getString("password"),
                    rs.getString("role")
                };
                model.addRow(data);
            }
            tbAkun.setModel(model);

        }catch(Exception e){
            
        }
        
    }

