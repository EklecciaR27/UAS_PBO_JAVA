    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try{
            java.sql.Statement stat=(java.sql.Statement)dbKoneksi.getKoneksi().createStatement();
            if(txtUsername.getText().equals("") ||
                txtNama.getText().equals("") ||
                txtPass.getText().equals("") ||
                cmbRole.getSelectedItem().toString().equals("")){
                JOptionPane.showMessageDialog(null, "DATA TIDAK BOLEH KOSONG");
                return;
            }
            // add data
            if(btnAdd.getText() == "ADD"){
                String cek = "SELECT * FROM tbbarang where idBarang = '" + txtUsername.getText() + "'";
                rs=stat.executeQuery(cek);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null, "Username Ini Sudah Ada...");
                }else{
                    String sql = "INSERT INTO tbakun VALUES('" + txtUsername.getText() +
                    "','" + txtNama.getText().toUpperCase() +
                    "','" + txtPass.getText().toUpperCase() +
                    "','" + cmbRole.getSelectedItem().toString().toUpperCase() + "')";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Data..");
                    tampildata();
                    clear();
                }
            }
            // ubah data
            else{
                String update = "UPDATE tbakun SET nama = '" + txtNama.getText() +
                "', password = '" + txtPass.getText() +
                "', role = '" + cmbRole.getSelectedItem().toString() +
                "' WHERE username = '" + txtUsername.getText() + "')";
                stat.executeUpdate(update);
                JOptionPane.showMessageDialog(null, "Data berhasil diubah..");
                tampildata();
                clear();
            }
        }catch(Exception e){

        }
    }//GEN-LAST:event_btnAddActionPerformed

       
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(txtUsername.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Silakan Pilih Data Yang di Hapus");
        }else{
            int jawab = JOptionPane.showInternalConfirmDialog(null,"Data ini akan diHapus, Lanjutkan ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(jawab==0){
                try{
                    java.sql.Statement stat=(java.sql.Statement)dbKoneksi.getKoneksi().createStatement();
                    String sql = "DELETE FROM tbakun WHERE username = '" + txtUsername.getText() + "'";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Berhasil diHapus..");
                    tampildata();
                    clear();
                } catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

