    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    String Jenis;
    Jenis = "";
    if (rbPipih.isSelected()){
        Jenis = "Pipih";
    }
    else if (rbTenggiri.isSelected()) {
        Jenis = "Tenggiri";
    }
        
        
        try{
            java.sql.Statement stat=(java.sql.Statement)dbKoneksi.getKoneksi().createStatement();
             if(txtID.getText().equals("") || 
                     txtNama.getText().equals("") || 
                     txtBerat.getText().equals("") || 
                     txtStok.getText().equals("") ||Jenis.equals("")){
                JOptionPane.showMessageDialog(null, "DATA TIDAK BOLEH KOSONG");
                return;
            }
            // add data
            if(btnAdd.getText() == "ADD"){
                String cek = "SELECT * FROM tbbarang where idBarang = '" + txtID.getText() + "'";
                rs=stat.executeQuery(cek);
                if (rs.next()){
                    JOptionPane.showMessageDialog(null, "ID Ini Sudah Ada...");
                }else{
                    String sql = "INSERT INTO tbbarang VALUES('" + txtID.getText() + 
                            "','" + txtNama.getText().toUpperCase() + 
                            "','" + txtBerat.getText().toUpperCase() + 
                            "','" + txtStok.getText().toUpperCase() + 
                            "','" + Jenis.toUpperCase() + "')";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Berhasil Menambahkan Data..");
                    tampildata();
                    clear();
                }
            }
            // ubah data
            else{
                String update = "UPDATE tbbarang SET namaBarang = '" + txtNama.getText() + 
                            "', beratBarang = '" + txtBerat.getText() + 
                            "', stokBarang = '" + txtStok.getText() + 
                            "', jenisIkan = '" + Jenis + 
                            "' WHERE idBarang = '" + txtID.getText() + "')";
                    stat.executeUpdate(update);
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah..");
                    tampildata();
                    clear();
            }
        }catch(Exception e){
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void tbDataBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataBarangMouseClicked
        
        
        txtID.setText(tbDataBarang.getValueAt(tbDataBarang.getSelectedRow(), 1).toString());
        txtNama.setText(tbDataBarang.getValueAt(tbDataBarang.getSelectedRow(), 2).toString());
        txtBerat.setText(tbDataBarang.getValueAt(tbDataBarang.getSelectedRow(), 3).toString());
        String jenis = tbDataBarang.getValueAt(tbDataBarang.getSelectedRow(), 4).toString();
            if (jenis.equals("TENGGIRI")) {
                rbTenggiri.setSelected(true);
                rbPipih.setSelected(false);
            } else if (jenis.equals("PIPIH")) {
                rbTenggiri.setSelected(false);
                rbPipih.setSelected(true);
            }
        txtStok.setText(tbDataBarang.getValueAt(tbDataBarang.getSelectedRow(), 5).toString());
        
        txtID.setEditable(false);
        btnAdd.setText("Ubah");
    }//GEN-LAST:event_tbDataBarangMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if(txtID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Silakan Pilih Data Yang di Hapus");
        }else{
            int jawab = JOptionPane.showInternalConfirmDialog(null,"Data ini akan diHapus, Lanjutkan ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
            if(jawab==0){
                try{
                    java.sql.Statement stat=(java.sql.Statement)dbKoneksi.getKoneksi().createStatement();
                    String sql = "DELETE FROM tbbarang WHERE idBarang = '" + txtID.getText() + "'";
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data Berhasil diHapus..");
                    tampildata();
                    clear();
                } catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

