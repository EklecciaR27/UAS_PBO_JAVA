    private void searching(){
    String search = "";

    int selectedIndex = cmbCari.getSelectedIndex();
    if (selectedIndex == 0) {
        search = "idBarang";
    } else if (selectedIndex == 1) {
        search = "namaBarang";
    }
        try{
            java.sql.Statement stat = (java.sql.Statement) dbKoneksi.getKoneksi().createStatement();
            rs = stat.executeQuery("SELECT * FROM tbbarang WHERE " + search + 
                                    " LIKE '%" + txtCari.getText() + "%'");

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No.");
            model.addColumn("ID");
            model.addColumn("Nama Barang");
            model.addColumn("Berat Barang");
            model.addColumn("Jenis Ikan");
            model.addColumn("Stok Barang");
            
            int no = 1 ;
            while (rs.next()) {
                Object[] data = {
                    no++,
                    rs.getString("idBarang"),
                    rs.getString("namaBarang"),
                    rs.getString("beratBarang"),
                    rs.getString("jenisIkan"),
                    rs.getString("stokBarang")
                };
                model.addRow(data);
            }
            tbDataBarang.setModel(model);

        }catch(Exception e){
            
        }
        
    }
    
    
    
    
